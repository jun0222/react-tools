export const buildPrompt = (word: string): string => {
  const w = word.trim();
  if (!w) return '（分解したい単語・概念を入力してください）';

  return `「${w}」について、以下のルールで説明してください。

【分解】
「${w}」を、理解に必要な最小限の要素・概念に分解してください。
まず全体像を1〜2文で示したあと、要素ごとに分けて説明してください。

【関係】
分解した要素どうしが、どう関係し合っているか（依存・順序・原因と結果など）を説明してください。
また、「${w}」が隣接する概念や全体の中でどう位置づけられるか（何の一部か、何と似ていて何と違うか）も教えてください。

【マーキング】
説明の中で、さらに分解しないと理解できなそうな用語や言い回しが出てきたら、その部分を【】で囲んでください。

【継続ルール】
このあと私が、あなたの回答に出てきた【】内の用語だけを送った場合は、その用語に対して今回と同じ手順（分解→関係→マーキング）で説明を続けてください。

では、「${w}」から始めてください。`;
};
