export const buildPrompt = (topic: string): string => {
  const t = topic.trim();
  if (!t) return '（対象領域を入力してください）';

  return `${t}において、「地に足をつけて議論を前に進めるための大前提となる万能な観点（問い）」を5つ作成してください。

作成にあたっては、以下の【条件】を厳守してください。

【条件】
- 綺麗事や抽象論ではなく、開発現場のリアルな空中戦を終わらせる「本質的で具体的な問い」であること
- 1文読んだだけで脳に刺さり、写経（書き写す）して思考の軸にしたくなるような、意味が凝縮された短い文であること
- 前後の文脈がなくても、その一文だけで意味が通じること
- 番号付きリストで提示すること`;
};