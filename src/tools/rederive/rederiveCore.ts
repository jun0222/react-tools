export const buildPrompt = (word: string): string => {
  const w = word.trim();
  if (!w) return '（概念・単語を入力してください）';

  return `「${w}」を完全に忘れたと仮定して、ゼロから論理的に再構築（再導出）する手助けをしてください。

【問題意識】
なぜこの概念が必要とされたのか、どんな問題や状況から生まれたのかを説明してください。

【原理からの構築】
その問題を解決するために、どんな原理・制約から、これが必然的に導かれるのかを一歩一歩示してください。

【再構築の道筋】
私がこの概念を完全に忘れても、この筋道さえ辿れば自力でもう一度たどり着けるように、再現の手順を示してください。`;
};
