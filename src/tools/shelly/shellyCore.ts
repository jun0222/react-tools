export const buildPrompt = (hint: string): string => {
  const h = hint.trim();
  const hintLine = h ? `\nヒント: ${h}` : '';

  return `POSIX標準（shで動作する共通規格）を前提に、現在のターミナルに一時的に登録して使う「使い捨てシェル関数」を作成します。

【質問は1つだけ】
実装に取り掛かる前に、この関数で「やりたいこと（目的）」を確認する質問を1つだけしてください。複数質問せず、1つに絞ってください。${hintLine}

【回答後の実装ルール】
私が目的を答えたら、以下を満たす関数コードを1つ出力してください。

- POSIX準拠のsh構文のみを使う（bash専用機能・配列・[[ ]] などは使わない）
- 引数の数・形式が不正な場合はエラーメッセージとusageを表示し、非0で終了する
- ファイルの変更・削除・上書きなど破壊的操作を伴う場合は、実行前に内容を表示する「ドライラン」を用意し、デフォルトはドライラン、明示的なフラグ（例: --apply）でのみ実行する
- コピペしてそのまま現在のシェルに貼り付けるだけで使える形にする（\`name() { ... }\` のPOSIX形式で定義する）
- 関数名は目的が分かる短い名前にする

出力は関数コードのみとし、説明は関数内の最小限のコメントに留めてください。`;
};